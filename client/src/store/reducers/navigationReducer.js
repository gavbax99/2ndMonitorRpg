const initState = {
	page: "Home",
};

const navigationReducer = (state = initState, action) => {
	switch (action.type) {
		
		// update the page
		case "NAVIGATE":
			return Object.assign({}, state, {
				page: action.page,
			});

		// first return the initState
		default:
			return initState;
	};
};

export default navigationReducer;